
public class NesterIfDemo {
 //Nested if statement
	
	public static void main(String[] args) {
		
     int x=30;
     int y=10;
     
     if(x == 30) {
    	 System.out.println("Parent condition is met");
    	 if(y == 10) {
    		System.out.println("x=30 and y=10"); 
    	 }
    	 
     }
     


	}

}

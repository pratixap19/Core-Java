
public class IfElseIfDemo {
    //if else if condition
	public static void main(String[] args) {
		int x=30;
		
		if(x == 10) {
			System.out.println("The value of x is 10");
			System.out.println("This is if block");
		}else if(x == 20) {
			System.out.println("The value of x is 20");
			System.out.println("This is an else if block");
			
		}else if(x == 30){
			System.out.println("The value of x is 30");
			System.out.println("This is an else if block");
		}else{
			System.out.println("This is an else statement");
		}
		

	}

}

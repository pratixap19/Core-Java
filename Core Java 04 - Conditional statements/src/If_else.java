
public class If_else {

	public static void main(String[] args) {

		int i = 10;
		int j = 20;
		int k = 30;
		
		if(i>j & j>k) {
			System.out.println("i is the smallest");
		}
		else if(i>j & j<k) {
			System.out.println("i is the smallest");
		}
		else {
			System.out.println("i is the smallest");
			
		}
	}

}

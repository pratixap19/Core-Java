//Object initialization by using reference variable
public class AnimalYoshi {
 
	String color;
    int age;
 
	public static void main(String[] args) {

    AnimalYoshi yoshi=new AnimalYoshi();
    yoshi.color = "black";
    yoshi.age = 13;
    
    System.out.println(yoshi.color+ " " + yoshi.age);
    
	}

}

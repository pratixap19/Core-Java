package com.JavaPracticePrograms;
//Write a program to print even numbers between 1 to 100.
public class CountEvenNumbers {

	public static void main(String[] args) {
		
          for(int i=1;i<=100;i++) {
        	  
        	  if(i%2==0)
        	  {
        		  System.out.println(i + " " + "is even number");
          }
	}
	
}
}



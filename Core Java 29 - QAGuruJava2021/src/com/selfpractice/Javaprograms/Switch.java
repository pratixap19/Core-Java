package com.selfpractice.Javaprograms;

public class Switch {

	public static void main(String[] args) {
	
		switch(1) {
		
		case 1:
		System.out.println("Monday");
		break;
		
		case 2:
			System.out.println("Tuesday");
			break;
			
		case 3:
			System.out.println("Wednesday");
			break;
			
			default :
				System.out.println("No day");
			
			
		
		
		}
	}

}

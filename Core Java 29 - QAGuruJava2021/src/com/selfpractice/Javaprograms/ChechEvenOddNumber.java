// A palindrome number is a number that remains the same when its digits are reversed. Ex. 16461
package com.selfpractice.Javaprograms;

public class ChechEvenOddNumber {

	public static void main(String[] args) {
		
		int a = 101;
		
		if(a % 2 == 0) {
			
			System.out.println("Even number");
		}
		else {
			
			System.out.println("Odd number");
		}
		
	}

}

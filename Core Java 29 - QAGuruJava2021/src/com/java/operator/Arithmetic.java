package com.java.operator;

public class Arithmetic {

	public static void main(String[] args) {

		int a = 12;
		int b = 5;

		int sum = a + b;
		
		int div = a/b;
		
		int sub = a-b;
		
		int mul = a*b;
		
		int rem = a % b;
		
		System.out.println(sum);
		System.out.println(div);
		System.out.println(sub);
		System.out.println(mul);
		System.out.println(rem);
	}

}
